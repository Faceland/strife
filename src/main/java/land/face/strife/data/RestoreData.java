package land.face.strife.data;

public class RestoreData {

  private float amount;
  private int ticks;

  public void setAmount(float amount) {
    this.amount = amount;
  }

  public void setTicks(int ticks) {
    this.ticks = ticks;
  }

  public float getAmount() {
    return amount;
  }

  public int getTicks() {
    return ticks;
  }

}
